export function getGuideByKey(key) {
	return TRAINING_GUIDES.find((guide) => guide.key === key) || null
}

export function getGuideByPath(path) {
	const currentPath = String(path || "")
	return (
		TRAINING_GUIDES.find((guide) =>
			(guide.matchers || []).some((matcher) => currentPath.includes(matcher))
		) || null
	)
}
